import java.util.Scanner;
public class Factorial1 {
	public static void main(String[] args) {
		Scanner object=new Scanner(System.in);
		System.out.println("enter the number");
		int a=object.nextInt();
		int fact=1;
		
		for(int i=a;i<=a;i--) {
			
			fact=fact*i;	
		}
		
		System.out.println(fact);
		
		
	}

}
